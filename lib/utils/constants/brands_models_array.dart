  // Helper method to get models for a specific brand and vehicle type
  List<String> getModels(String? brand, String? vehicleType) {
    if (brand == null || vehicleType == null) {
      return [];
    }

    if (brandModelsByType.containsKey(brand) &&
        brandModelsByType[brand]!.containsKey(vehicleType)) {
      return brandModelsByType[brand]![vehicleType]!;
    }

    return [];
  }

  // Get all brands
  List<String> get brands => brandModelsByType.keys.toList();

  // Get all vehicle types available for a specific brand
  List<String> getVehicleTypesForBrand(String brand) {
    if (brandModelsByType.containsKey(brand)) {
      return brandModelsByType[brand]!.entries
          .where((entry) => entry.value.isNotEmpty)
          .map((entry) => entry.key)
          .toList();
    }
    return [];
  }
